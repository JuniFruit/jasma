
# 1 Day in seconds
DAY_IN_SECONDS = 86400
# 7 Days in seconds
WEEK_IN_SECONDS = DAY_IN_SECONDS * 7
# WEEK_IN_SECONDS = 604800
# 30 Days in seconds
MONTH_IN_SECONDS = DAY_IN_SECONDS * 30
# MONTH_IN_SECONDS = 2592000
# 1 Second in miliseconds
SECOND_IN_MS = 1000
# half a minute in miliseconds
HALF_MINUTE_IN_MS = SECOND_IN_MS * 30
# 1 Minute in miliseconds
MINUTE_IN_MS = SECOND_IN_MS * 60
# half hour in miliseconds
HALF_HOUR_IN_MS = MINUTE_IN_MS * 30
# 1 Hour in miliseconds
HOUR_IN_MS = MINUTE_IN_MS * 60
